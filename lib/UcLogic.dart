library upharm;

/// A Calculator.
class UcLogic {
  /// Returns [value] plus 1.
  int add(int value1, int value2) => value1 + value2;

  int sub(int value1, int value2) => value1 + value2;

  int div(int value1, int value2) => value1 + value2;

  int mul(int value1, int value2) => value1 + value2;
}
